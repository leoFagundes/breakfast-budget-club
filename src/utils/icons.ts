import * as Icons from "lucide-react";

export function normalizeIconName(input?: string) {
  if (!input) return "";

  return input
    .trim()
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join("");
}

